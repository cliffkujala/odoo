import time

from openerp.osv import osv
from openerp.report import report_sxw
from common_report_header import common_report_header


class account_balance(osv.AbstractModel, common_report_header):
    _name = 'report.account.account.balance'

    def _get_account(self, data):
        if data['model']=='account.account':
            return self.pool.get('account.account').browse(self.cr, self.uid, data['form']['id']).company_id.name
        return super(account_balance ,self)._get_account(data)

    def render_html(self, cr, uid, ids, data=None, context=None):
        report_obj = self.pool['report']
        report = report_obj._get_report_from_name(cr, uid, 'account.report_trialbalance')

        docargs = {
            'doc_model': report.model,
            'docs':_get_account,
        }
        return report_obj.render(cr, uid, ids, 'account.report_trialbalance', docargs, context=context)

